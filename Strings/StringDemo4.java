/* In this program, we are converting the string to uppercase using toUpperCase() method.
 * The toUpperCase() method converts the given string to uppercase.
 * The toUpperCase() method is a non-static method of String class.
 * The toUpperCase() method is used to convert the given string to uppercase.
 */

public class StringDemo4 { //String data type
    public static void main(String[] args) { //main method
        String s1 = new String("Houston"); //String data type
        s1 = s1.toUpperCase(); //toUpperCase() method to convert the string to uppercase
        System.out.println(s1); //print the value of s1
    }
}
